:- use_module(library(aleph)).
:- if(current_predicate(use_rendering/1)).
:- use_rendering(prolog).
:- endif.
:- aleph.
:- style_check(-discontiguous).
:- aleph_set(verbosity,1).
:- aleph_set(minpos,5).
:- aleph_set(nodes,100000).
:- modeh(1,cyclic(+id)).
:- aleph_set(clauselength,15).
:- modeb(*,edge(+id,-id)).
:- modeb(*,node(+id,-color)).
:- modeb(*,green(+color)).
:- modeb(*,red(+color)).
:- determination(cyclic/1,edge/2).
:- determination(cyclic/1,node/2).
:- determination(cyclic/1,green/1).
:- determination(cyclic/1,red/1).
