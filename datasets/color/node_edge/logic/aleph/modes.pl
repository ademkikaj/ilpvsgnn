:- use_module(library(aleph)).
:- if(current_predicate(use_rendering/1)).
:- use_rendering(prolog).
:- endif.
:- aleph.
:- style_check(-discontiguous).
:- aleph_set(verbosity,1).
:- aleph_set(minpos,5).
:- aleph_set(nodes,100000).
:- modeh(1,color(+id,+node_id)).
:- aleph_set(clauselength,7).
:- modeb(*,node(+id,-node_id,-color)).
:- modeb(*,edge(+id,-node_id,-node_id)).
:- modeb(*,same_color(+color,-color)).
:- modeb(*,red(+color)).
:- modeb(*,green(+color)).
:- determination(color/1,node/3).
:- determination(color/1,edge/3).
:- determination(color/1,same_color/2).
:- determination(color/1,red/1).
:- determination(color/1,green/1).
